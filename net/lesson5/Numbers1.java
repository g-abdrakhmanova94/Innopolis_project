package net.lesson5;

import java.util.Scanner;

public class Numbers1 {

    public static void main(String[] args) {
        int a = new Scanner(System.in).nextInt();

        if (a % 2 == 0) {
            System.out.println("Четное число.");
        }
        else {
            System.out.println("Нечетное число.");
            }
        }
    }